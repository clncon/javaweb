package cn.itcast.java.domain;

public class Type {
	private int id;
	private String title;

	public Type() {
	}

	public int getId() {
		return id ;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
   
}
