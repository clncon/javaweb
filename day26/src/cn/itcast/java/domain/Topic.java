package cn.itcast.java.domain;

public class Topic {
    private int id;
    private String title;
    
    public Topic(){}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
    
}
